def t(locale: str, key: str, **kwargs) -> str:
    """번역 문자열을 반환한다. 없으면 한국어 폴백."""
    loc = locale if locale in MESSAGES else "ko"
    table = MESSAGES.get(loc) or MESSAGES["ko"]
    text = table.get(key) or MESSAGES["ko"].get(key) or key
    if kwargs:
        return text.format(**kwargs)
    return text


def format_price(locale: str, value) -> str:
    if value is None or value == "":
        return ""
    try:
        n = int(value)
    except (TypeError, ValueError):
        return str(value)
    return t(locale, "currency_fmt", n=n)
